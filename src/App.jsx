    '# ═══════════════════════════════════════════════════',
    '# Screen "cracks" radiate from center',
    'crack_chars=("/" "\\\\" "|" "-" "+" "X" "*")',
    'for ((cr=1;cr<=25;cr++)); do',
    '  for angle in 0 1 2 3 4 5 6 7; do',
