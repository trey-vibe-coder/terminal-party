    '  for ((attempts=0;attempts<ring*3+4;attempts++)); do',
    '    # Pick random point at approximately this ring distance',
    '    angle=$((RANDOM%360))',
    '    rx=$((MX + (ring * (RANDOM%3-1+1)) * (2*(angle%2)-1) / 2 ))',
    '    ry=$((MY + (ring/2) * (2*((angle/90)%2)-1) ))',
    '    ((rx<1)) && rx=1; ((rx>COLS)) && rx=$COLS',
    '    ((ry<1)) && ry=1; ((ry>LNS)) && ry=$LNS',
    '    hc="${HEX_COLORS[$((RANDOM%4))]}"',
    '    cur $ry $rx; out "${hc}${HX[$((RANDOM%${#HX[@]}))]}${RS}"',
    '  done',
    '  sleep 0.008',
    'done',
    '# Fill any remaining gaps',
    'for ((y=1;y<=LNS;y++)); do',
    '  for ((x=1;x<=COLS;x+=3)); do',
    '    cur $y $x; out "${G}${HX[$((RANDOM%${#HX[@]}))]}${RS}"',
    '  done',
    'done',
    'sleep 0.4',
    '# Fade out in waves from edges inward',
    'for ((ring=MX;ring>=0;ring-=3)); do',
    '  for ((i=0;i<$((COLS*LNS/12));i++)); do',
    '    fx=$((RANDOM%COLS+1)); fy=$((RANDOM%LNS+1))',
    '    dist=$(( (fx-MX)*(fx-MX)/4 + (fy-MY)*(fy-MY) ))',
    '    limit=$((ring*ring/4))',
    '    ((dist>=limit)) && { cur $fy $fx; out " "; }',
    '  done',
    '  sleep 0.03',
    'done',
    'sleep 0.1; clear',
    '',
    '# ═══════════════════════════════════════════════════',
    '# PHASE 3.5: SCREEN GLITCH (chaos flicker)',
    '# ═══════════════════════════════════════════════════',
    'GL=("E" "R" "R" "O" "R" " " "?" "!" "#" "@" "%" "&" "/" "\\\\" "|" "~")',
    'for ((glitch=0;glitch<3;glitch++)); do',
    '  for ((gy=1;gy<=LNS;gy+=2)); do',
    '    cur $gy 1; buf=""',
    '    for ((gx=1;gx<=COLS;gx++)); do',
    '      buf+="${GL[$((RANDOM%${#GL[@]}))]}"',
    '    done',
    '    out "${BD}${CL[$((RANDOM%7))]}${buf}${RS}"',
    '  done',
    '  sleep 0.04',
    '  # "shift" — redraw offset lines',
    '  for ((gy=2;gy<=LNS;gy+=2)); do',
    '    cur $gy 1; buf=""',
    '    for ((gx=1;gx<=COLS;gx++)); do',
    '      buf+="${GL[$((RANDOM%${#GL[@]}))]}"',
    '    done',
    '    out "${DM}${CL[$((RANDOM%7))]}${buf}${RS}"',
    '  done',
    '  sleep 0.03',
    'done',
    '# flash black/white',
    'for ((zz=0;zz<2;zz++)); do',
    '  clear; sleep 0.04',
    '  for ((gy=1;gy<=LNS;gy++)); do',
    '    cur $gy 1; printf "%*s" "$COLS" "" | tr " " "#"',
    '  done',
    '  sleep 0.03',
    'done',
    'clear',
    '',
    '# ═══════════════════════════════════════════════════',
    '# DISCO FLOOR',
