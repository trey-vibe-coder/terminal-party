    'done',
    '',
    '# Light rays from disco ball',
    'for ((ray=0;ray<12;ray++)); do',
    '  rx=$((MX + (ray-6)*6 + RANDOM%3))',
    '  for ((ry=7;ry<7+ray%4+2;ry++)); do',
    '    ((rx>0 && rx<=COLS && ry>0 && ry<=LNS)) && {',
    '      cur $ry $rx; out "${DM}${CL[$((RANDOM%7))]}|${RS}"',
    '    }',
    '  done',
    'done',
    'sleep 0.2',
    '',
    '# Animated checkerboard dance floor with colored tiles',
    'DBLK=("█" "▓" "▒" "░")',
    'DCL=("\\033[41m" "\\033[42m" "\\033[43m" "\\033[44m" "\\033[45m" "\\033[46m" "\\033[47m")',
    'FLOOR_TOP=$((LNS/2))',
    'for ((disco=0;disco<24;disco++)); do',
    '  # Redraw disco ball sparkle',
    '  cur 1 $DB_X',
    '  if ((disco%3==0)); then out "${BD}${Y}     .----.     ${RS}"',
    '  elif ((disco%3==1)); then out "${BD}${W}     .----.     ${RS}"',
